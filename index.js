const sum = (n1, n2) => {
    return n1 + n2;
}

const minus = (n1, n2) => {
    return n1 - n2;
}

const mult = (n1, n2) => {
    return n1 * n2;
}

const divide = (n1, n2) => {
    return n1 / n2;
}

const square = (n) => {
    return n ^ 2;
}

module.exports = {sum, minus, mult, divide, square};
